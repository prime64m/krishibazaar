function setLanguage(lang) {
    // Update the title
    document.getElementById("title").innerText = translations[lang].title;

    // Update table headers
    document.getElementById("crop-name").innerText = translations[lang].cropName;
    document.getElementById("current-bid").innerText = translations[lang].currentBid;
    document.getElementById("min-bid").innerText = translations[lang].minBid;
    document.getElementById("order").innerText = translations[lang].order;
    document.getElementById("contact").innerText = translations[lang].contact;

    // Update contact section
    document.getElementById("contact-title").innerText = translations[lang].contactTitle;
    document.getElementById("contact-info").innerText = translations[lang].contactInfo;

    // Update crop table
    const cropTableBody = document.getElementById("crop-table-body");
    cropTableBody.innerHTML = ""; // Clear existing rows

    translations[lang].crops.forEach(crop => {
        const row = document.createElement("tr");
        row.innerHTML = `
            <td>${crop.name}</td>
            <td>${crop.currentBid}</td>
            <td>${crop.minBid}</td>
            <td>${crop.order}</td>
            <td>${crop.contact}</td>
        `;
        cropTableBody.appendChild(row);
    });
}

// Set default language to English on page load
window.onload = () => {
    setLanguage('en');
};